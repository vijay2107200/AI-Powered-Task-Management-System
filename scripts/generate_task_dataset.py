"""Synthetic task management dataset generator.

Produces data/raw/tasks.csv: 8,000 software/project tasks with free-text
descriptions, categories, priorities, deadlines and assignee workload
fields, plus realistic data-quality problems (missing values, duplicate
records, inconsistent labels) that are handled during Week 1 cleaning.

Run from the repository root:
    python scripts/generate_task_dataset.py
"""
import random
from datetime import date, timedelta

import numpy as np
import pandas as pd

SEED = 42
random.seed(SEED)
np.random.seed(SEED)

N_TASKS = 8000
N_USERS = 25

COMPONENTS = [
    "login page", "payment gateway", "user dashboard", "search API",
    "notification service", "mobile app", "admin panel", "report module",
    "checkout flow", "profile settings", "file upload service",
    "email scheduler", "analytics pipeline", "inventory module",
    "chat widget", "order tracking page", "billing system",
    "authentication service", "landing page", "database layer",
]

TEMPLATES = {
    "Bug Fix": [
        "Fix the crash in the {c} when the user submits an empty form",
        "Resolve the 500 error thrown by the {c} under heavy load",
        "Fix broken layout of the {c} on small screens",
        "Investigate and fix duplicate records created by the {c}",
        "Fix incorrect totals displayed in the {c} after a refund",
        "Patch the memory leak in the {c} background worker",
        "Fix the timeout issue in the {c} when the network is slow",
        "Correct the wrong currency formatting shown on the {c}",
    ],
    "Feature": [
        "Implement dark mode support for the {c}",
        "Add bulk export to CSV from the {c}",
        "Build a filtering and sorting option for the {c}",
        "Implement two-factor authentication in the {c}",
        "Add multi-language support to the {c}",
        "Create an auto-save draft feature for the {c}",
        "Implement pagination and lazy loading for the {c}",
        "Add role-based access control to the {c}",
    ],
    "Documentation": [
        "Write the user guide for the {c}",
        "Update the API reference after the recent changes to the {c}",
        "Document the deployment steps for the {c}",
        "Create onboarding notes explaining how the {c} works",
        "Add code comments and docstrings to the {c} module",
        "Prepare a troubleshooting FAQ for the {c}",
    ],
    "Testing": [
        "Write unit tests covering edge cases of the {c}",
        "Add integration tests for the {c} endpoints",
        "Set up automated regression tests for the {c}",
        "Perform load testing on the {c} before release",
        "Review and improve test coverage of the {c}",
        "Create end-to-end test scenarios for the {c}",
    ],
    "Deployment": [
        "Deploy the latest release of the {c} to production",
        "Set up the staging environment for the {c}",
        "Configure CI/CD pipeline for the {c}",
        "Roll back the faulty release of the {c} and verify stability",
        "Migrate the {c} to the new cloud infrastructure",
        "Containerize the {c} and update the deployment scripts",
    ],
    "Maintenance": [
        "Upgrade outdated dependencies used by the {c}",
        "Clean up dead code and unused assets in the {c}",
        "Optimize slow database queries behind the {c}",
        "Rotate credentials and update secrets for the {c}",
        "Archive old logs generated by the {c}",
        "Refactor the {c} to reduce technical debt",
    ],
    "Research": [
        "Evaluate third-party libraries for improving the {c}",
        "Research caching strategies to speed up the {c}",
        "Compare hosting options for scaling the {c}",
        "Prototype a machine learning approach for the {c}",
        "Analyze user feedback collected from the {c}",
        "Benchmark current performance of the {c} and report findings",
    ],
}

CATEGORIES = list(TEMPLATES)
CATEGORY_WEIGHTS = [0.28, 0.24, 0.10, 0.14, 0.09, 0.10, 0.05]

URGENT_MARKERS = ["URGENT: ", "ASAP - ", "[HIGH IMPACT] ", ""]

STATUSES = ["Open", "In Progress", "Completed", "Blocked"]
STATUS_WEIGHTS = [0.35, 0.25, 0.32, 0.08]

# typical effort (hours) by category: (mean, sigma) of a lognormal
EFFORT = {
    "Bug Fix": (1.4, 0.5), "Feature": (2.4, 0.5), "Documentation": (1.2, 0.4),
    "Testing": (1.7, 0.5), "Deployment": (1.5, 0.5), "Maintenance": (1.6, 0.5),
    "Research": (2.0, 0.5),
}

users = [f"USER-{i:02d}" for i in range(1, N_USERS + 1)]
user_experience = {u: round(np.random.uniform(0.5, 15.0), 1) for u in users}
user_workload = {u: np.random.randint(1, 21) for u in users}

rows = []
start = date(2026, 1, 5)
for i in range(N_TASKS):
    cat = random.choices(CATEGORIES, weights=CATEGORY_WEIGHTS, k=1)[0]
    desc = random.choice(TEMPLATES[cat]).format(c=random.choice(COMPONENTS))

    est = float(np.round(np.random.lognormal(*EFFORT[cat]), 1))
    est = min(est, 80.0)

    created = start + timedelta(days=int(np.random.uniform(0, 180)))
    # deadlines: bugs get tighter deadlines than research/docs
    base_days = {"Bug Fix": 5, "Feature": 21, "Documentation": 14, "Testing": 10,
                 "Deployment": 7, "Maintenance": 18, "Research": 28}[cat]
    deadline_days = max(1, int(np.random.gamma(2.0, base_days / 2)))
    due = created + timedelta(days=deadline_days)

    user = random.choice(users)

    # priority driven by deadline pressure, effort and category
    score = (
        2.2 / max(deadline_days, 1)
        + est / 30.0
        + (0.30 if cat in ("Bug Fix", "Deployment") else 0.0)
        + np.random.normal(0, 0.25)
    )
    if score > 1.30:
        prio = "Critical"
    elif score > 0.85:
        prio = "High"
    elif score > 0.45:
        prio = "Medium"
    else:
        prio = "Low"

    if prio in ("Critical", "High") and random.random() < 0.18:
        desc = random.choice(URGENT_MARKERS) + desc

    sp_choices = [1, 2, 3, 5, 8, 13]
    sp = sp_choices[min(int(est // 6), 5)] if random.random() > 0.5 else \
        random.choice(sp_choices)

    rows.append({
        "task_id": f"TASK-{10000 + i}",
        "task_description": desc,
        "category": cat,
        "priority": prio,
        "status": random.choices(STATUSES, weights=STATUS_WEIGHTS, k=1)[0],
        "created_date": created.isoformat(),
        "due_date": due.isoformat(),
        "estimated_hours": est,
        "story_points": sp,
        "assignee_id": user,
        "assignee_experience_years": user_experience[user],
        "assignee_open_tasks": user_workload[user],
    })

df = pd.DataFrame(rows)

# ---- inject realistic data-quality problems (to be handled in cleaning) ----
rng = np.random.default_rng(SEED)

# 1) missing values
df.loc[rng.choice(N_TASKS, 180, replace=False), "estimated_hours"] = np.nan
df.loc[rng.choice(N_TASKS, 120, replace=False), "assignee_experience_years"] = np.nan
df.loc[rng.choice(N_TASKS, 60, replace=False), "story_points"] = np.nan

# 2) inconsistent category labels (casing / stray whitespace)
idx = rng.choice(N_TASKS, 250, replace=False)
df.loc[idx[:90], "category"] = df.loc[idx[:90], "category"].str.lower()
df.loc[idx[90:170], "category"] = df.loc[idx[90:170], "category"].str.upper()
df.loc[idx[170:], "category"] = df.loc[idx[170:], "category"] + " "

# 3) whitespace noise in some descriptions
idx = rng.choice(N_TASKS, 150, replace=False)
df.loc[idx, "task_description"] = "  " + df.loc[idx, "task_description"] + "   "

# 4) duplicate records appended at the end
dupes = df.sample(n=110, random_state=SEED)
df = pd.concat([df, dupes], ignore_index=True)
df = df.sample(frac=1.0, random_state=SEED).reset_index(drop=True)

out = "data/raw/tasks.csv"
df.to_csv(out, index=False)
print(f"Wrote {len(df)} rows to {out}")
print(df["category"].str.strip().str.title().value_counts().to_string())
print(df["priority"].value_counts().to_string())
